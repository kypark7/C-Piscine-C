#include <unistd.h>

void		ft_write(int c)
{
	char	n[2];

	n[0] = '0';
	n[1] = n[0] + c / 10;
	n[0] = n[0] + c % 10;
	write(1, &n[1], 1);
	write(1, &n[0], 1);
}

void		ft_print(int n, int m)
{
	ft_write(n);
	write(1, " ", 1);
	ft_write(m);
	if (n != 98 && n != 99)
	{
		write(1, ", ", 2);
	}
}

void		ft_print_comb2(void)
{
	int		i[2];
	char	j[2];

	i[0] = 0;
	j[0] = ' ';
	j[1] = ',';
	while (i[0] < 99)
	{
		i[1] = i[0] + 1;
		while (i[1] < 100)
		{
			ft_print(i[0], i[1]);
			i[1]++;
		}
		i[0]++;
	}
}
