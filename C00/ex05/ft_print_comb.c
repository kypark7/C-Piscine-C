#include <unistd.h>

void	ft_putchar(char c, char c1, char c2)
{
	write(1, &c, 1);
	write(1, &c1, 1);
	write(1, &c2, 1);
}

void	ft_print_comb(void)
{
	char n[6];

	n[0] = '0';
	n[3] = ',';
	n[4] = ' ';
	while (n[0] <= '7')
	{
		n[1] = n[0] + 1;
		while (n[1] <= '8')
		{
			n[2] = n[1] + 1;
			while (n[2] <= '9')
			{
				ft_putchar(n[0], n[1], n[2]);
				if (!((n[0] == '7') && (n[1] == '8') && (n[2] == '9')))
				{
					write(1, &n[3], 1);
					write(1, &n[4], 1);
				}
				n[2]++;
			}
			n[1]++;
		}
		n[0]++;
	}
}
