int				ft_atoi(char *s)
{
	int			i;
	int			minus;
	long long	answer;

	i = 0;
	minus = 1;
	answer = 0;
	while (s[i] == ' ' || (s[i] >= 9 && s[i] <= 13))
		i++;
	while ((s[i] == '-') || (s[i]) == '+')
	{
		if (s[i] == '-')
			minus *= -1;
		++i;
	}
	while (s[i] && ((s[i] >= '0' && s[i] <= '9')))
	{
		answer = answer * 10 + s[i] - '0';
		i++;
	}
	return ((int)answer * minus);
}
